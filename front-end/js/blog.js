// Render blog cards from blogData
function renderBlogGrid() {
    if (!blogGrid) return;
    blogGrid.innerHTML = '';

    Object.keys(blogData).forEach(id => {
        const b = blogData[id];
        const card = document.createElement('article');
        card.className = 'blog-card';
        card.setAttribute('data-id', id);
        card.setAttribute('data-category', b.category);
        card.innerHTML = `
            <div class="blog-image" style="background-image:url('${b.image}')"></div>
            <div class="blog-content">
                <div class="blog-category ${b.category}-category">${b.categoryName}</div>
                <h3 class="blog-title">${b.title}</h3>
                <p class="blog-excerpt">${stripHtml(b.content).slice(0, 150)}...</p>
                <div class="blog-meta"><small>${b.author} • ${b.date} • ${b.readTime}</small></div>
            </div>
        `;

        card.addEventListener('click', () => showBlogDetail(id));
        blogGrid.appendChild(card);
    });
}

// Utility to strip HTML for excerpts
function stripHtml(html) {
    const tmp = document.createElement('div');
    tmp.innerHTML = html;
    return tmp.textContent || tmp.innerText || '';
}

// Filter handling
filterButtons.forEach(btn => {
    btn.addEventListener('click', () => {
        filterButtons.forEach(b => b.classList.remove('active'));
        btn.classList.add('active');
        const category = btn.getAttribute('data-category');

        document.querySelectorAll('.blog-card').forEach(card => {
            if (category === 'all' || card.getAttribute('data-category') === category) {
                card.style.display = '';
            } else {
                card.style.display = 'none';
            }
        });
    });
});

// Detail view
function showBlogDetail(blogId) {
    const blog = blogData[blogId];
    if (!blog) return;

    detailCategory.textContent = blog.categoryName;
    detailCategory.className = `detail-category ${blog.category}-category`;
    detailTitle.textContent = blog.title;
    detailImage.style.backgroundImage = `url('${blog.image}')`;
    detailContent.innerHTML = blog.content;

    const metaElements = document.querySelectorAll('.detail-meta span');
    if (metaElements.length >= 3) {
        metaElements[0].innerHTML = `<i class="far fa-user"></i> ${blog.author}`;
        metaElements[1].innerHTML = `<i class="far fa-calendar"></i> ${blog.date}`;
        metaElements[2].innerHTML = `<i class="far fa-clock"></i> ${blog.readTime}`;
    }

    // Toggle views
    if (blogListSection) blogListSection.style.display = 'none';
    if (blogDetailSection) blogDetailSection.style.display = 'block';
    window.scrollTo(0, 0);
}

